class MediaSimples {
	public static void main(String[] args) {
		Double numero1 = 25.0;
		Double numero2 = 39.0;
		Double resultado;
		Double media = 0.0;
		

		resultado = numero1 + numero2;
		media = resultado/2;
	
		System.out.println("Número 1: " + numero1);
		System.out.println("Número 2: " + numero2);
		System.out.println("A média é: " + media);
	}
}